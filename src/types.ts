export type BundlerResults =
  | Promise<{
      code: any;
      map: string | undefined;
      assets: any;
      symlinks: any;
      stats: any;
    }>
  | {
      close(): void;
      handler(handler: any): void;
      rebuild(handler: any): void;
    };

export type BundlerOptions = {
  cache?: boolean;
  customEmit?: boolean;
  esm?: boolean;
  externals?: any[];
  filename?: string;
  minify?: boolean;
  sourceMap?: boolean;
  sourceMapRegister?: boolean;
  sourceMapBasePrefix?: string;
  assetBuilds?: boolean;
  watch?: boolean | { [key: string]: any };
  v8cache?: boolean;
  filterAssetBase?: PathLike | string;
  existingAssetNames?: any[];
  quiet?: boolean;
  debugLog?: boolean;
  transpileOnly?: boolean;
  license?: string;
  target?: keyof CompilerOptions['target'] | string;
  production?: boolean;
  // webpack defaults to `module` and `main`, but that's
  // not really what node.js supports, so we reset it
  mainFields?: string[] | ['main'];
};
export type Bundler = (
  pathToEntry: string,
  opts?: BundlerOptions
) => BundlerResults;

export type PromiseResolver = (value: unknown) => void;

export type TSConfig = {
  compilerOptions: CompilerOptions;
  include?: string[];
  exclude?: string[];
};
export interface TerserMinifyOutput extends MinifyOutput {
  map?: any;
}

